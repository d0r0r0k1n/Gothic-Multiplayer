function OnFilterscriptInit()
	Enable_OnPlayerKey(1);
	
	if io.open("vob.me","r+")==nil then
		local VobFile=io.open("vob.me","w+");
		VobFile:close();
	else
		
		InitObjects();
	end
	print("--------------------");
	print("|Vob Edit Workspace|");
	print("|           (R)Dick|");
	print("--------------------");
end

function InitObjects()
	local VobFile=io.open("vob.me","r+");
	local filestream=VobFile:read();
	local result,name,rotatex,rotatey,rotatey,posx,posy,posz;
	local i=0;
	for line in io.lines("vob.me","r+") do
		result,name,rotatex,rotatey,rotatez,posx,posy,posz=sscanf(filestream,"sdddddd");
		if result==1 then
			LoadedObject[i]=Vob.Create(name,"KHORINIS.ZEN",posx,posy,posz);
			LoadedObject[i]:SetRotation(rotatex,rotatey,rotatez);
		end
		filestream=VobFile:read("*l");
		i=i+1;
	end
	VobFile:close();
end

