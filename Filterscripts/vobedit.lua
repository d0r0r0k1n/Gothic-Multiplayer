function VobEditing(playerid)
	--if Editor[playerid].object[Editor[playerid].object_count]~=nil then
		
	--end
	x,y,z=GetPlayerPos(playerid);
	cy=y+Editor[playerid].posY;
	cz=Editor[playerid].distance_to_object*math.cos(GetPlayerAngle(playerid)*3.14/180);
	cx=Editor[playerid].distance_to_object*math.sin(GetPlayerAngle(playerid)*3.14/180);
	--print(cx," ",cz);
	--print(x,z)
	--print(cx+x,cz+z);
	--print(Editor[playerid].object_count);
	Editor[playerid].model=Objectlist[Editor[playerid].nmodel];
	if Editor[playerid].changemodel==1 then
		if Editor[playerid].object[Editor[playerid].object_count]~=nil then
			Editor[playerid].object[Editor[playerid].object_count]:Destroy();
		end
		Editor[playerid].object[Editor[playerid].object_count]=Vob.Create(Objectlist[Editor[playerid].nmodel],GetPlayerWorld(playerid),x+cx,cy,z+cz);
		Editor[playerid].changemodel=0;
	end
	Editor[playerid].object[Editor[playerid].object_count]:SetPosition(cx+x,cy,cz+z);
	Editor[playerid].object[Editor[playerid].object_count]:SetRotation(Editor[playerid].rotateX,Editor[playerid].rotateY,Editor[playerid].rotateZ);
end

